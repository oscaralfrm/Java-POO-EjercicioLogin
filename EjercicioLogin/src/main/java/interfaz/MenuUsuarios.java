    private void cargarTabla() {

        // Definimos el modelo de la tabla
        DefaultTableModel modeloTabla = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        String[] columnasTabla = {"ID", "Nombre de Usuario", "Rol"};

        modeloTabla.setColumnIdentifiers(columnasTabla);

        tblUsuarios.setModel(modeloTabla);

        // Traemos todos los registros de usuarios
        ArrayList<Usuario> listaDeUsuarios = controlador.traerUsuarios();

        // Recorremos el arreglo de usuarios
        // Si no est√° vac√≠a
        if (listaDeUsuarios != null) {
            for (Usuario usuario : listaDeUsuarios) {

                // Caragmos todo, menos la contrase√±a, obviamente
                // Mostramos adem√°s el nombre del rol del usuario.getRolUsuario().getNombreRol()
                Object[] registroUsuario = {usuario.getId(), usuario.getNombreUsuario(), usuario.getRolUsuario().getNombreRol()};

                modeloTabla.addRow(registroUsuario);

            }

        }
    }

